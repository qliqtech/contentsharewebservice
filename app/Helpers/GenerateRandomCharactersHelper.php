<?php


namespace App\Helpers;


class GenerateRandomCharactersHelper
{

    public static function incrementalHash($len = 5){
        $charset = "123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ";
        $base = strlen($charset);
        $result = '';

        $now = explode(' ', microtime())[1];
        while ($now >= $base){
            $i = $now % $base;
            $result = $charset[$i] . $result;
            $now /= $base;
        }
        return substr($result, -$len);
    }

